export interface University {
  id: string
  name: string
  ranking: string
  location: string
  tuitionFee: string
  popularPrograms: string[]
}

export interface Destination {
  id: string // e.g. 'united-kingdom', 'united-states'
  name: string
  description: string
  fullDescription: string
  region: 'na' | 'eu' | 'ap'
  popularity: 'high' | 'emerging'
  budget: 'eco' | 'mid' | 'prem'
  flag: string
  imageUrl: string
  visaSuccess: string
  workPermit: string
  costOfLiving: string
  ieltsRequirement: string
  demandSectors: string[]
  universities: University[]
}

export interface Course {
  id: string
  title: string
  category: 'ai-tech' | 'languages' | 'career-dev' | 'study-abroad'
  duration: string
  lessons: number
  rating: number
  instructor: string
  description: string
  enrolled: boolean
  progress: number
}

export interface TestPrep {
  id: string
  name: string
  fullName: string
  description: string
  format: string
  duration: string
  scoreRange: string
  sections: string[]
  resources: { title: string; type: 'guide' | 'practice-test' | 'video' }[]
}

